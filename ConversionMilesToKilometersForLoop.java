public class ConversionMilesToKilometersForLoop {
    public static void main(String[] args) {
        final double MILES_TO_KILOMETERS = 1.609;
        double kilometersDerivedFromMiles;
        System.out.println("Miles    Kilometers");
        for (int miles = 1; miles <=10; miles ++){
            kilometersDerivedFromMiles = miles * MILES_TO_KILOMETERS;
            System.out.printf("%-2d%7s%1.3f\n", miles,"       ",kilometersDerivedFromMiles);
        }
    }


}



























































































































